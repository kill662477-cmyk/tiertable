// name -> { tier, race, initialMMR } 평탄화
function buildSeedIndex() {
  const map = new Map();
  for (const t of TIERS) {
    for (const race of ["T", "Z", "P"]) {
      for (const name of t[race]) {
        if (map.has(name)) {
          console.warn(`[seed] 중복 이름 감지: ${name} (티어 ${map.get(name).tier} / ${t.id})`);
        }
        map.set(name, { tier: t.id, race, initialMMR: TIER_BASELINE_MMR[t.id] });
      }
    }
  }
  return map;
}

